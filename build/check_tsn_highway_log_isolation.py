"""CMP-AUD-132: TSN Highway Log conversion is attempt-isolated.

This is deliberately a control-flow check, not a PDF parser check.  Tiny fake PDFs
and monkeypatched parse/write/combine seams drive the production ``consolidate``
function through overlapping attempts.  Each final build must receive an explicit
manifest containing only the workbooks generated by that attempt, and custom builds
must never inspect, clear, write, or glob the module-global conversion directory.
"""
from __future__ import annotations

import shutil
import sys
import tempfile
import threading
from pathlib import Path
from unittest.mock import patch

ROOT = Path(__file__).resolve().parents[1]
sys.path.insert(0, str(ROOT / "scripts"))

import consolidate_tsn_highway_log as target  # noqa: E402
from consolidate_xlsx_base import consolidate_xlsx as real_consolidate_xlsx  # noqa: E402
from events import ConsolidateResult, Events  # noqa: E402
from openpyxl import Workbook, load_workbook  # noqa: E402


failures = []


def check(label, condition):
    mark = "OK  " if condition else "FAIL"
    print(f"{mark} {label}")
    if not condition:
        failures.append(label)


def touch_pdf(folder, name):
    folder.mkdir(parents=True, exist_ok=True)
    (folder / name).write_bytes(b"fixture")


def snapshot(folder):
    if not folder.exists():
        return {}
    return {p.name: p.read_bytes() for p in sorted(folder.iterdir()) if p.is_file()}


def no_subdirs(folder):
    return not folder.exists() or not any(p.is_dir() for p in folder.iterdir())


with tempfile.TemporaryDirectory(prefix="tsn-hl-isolation-") as td:
    root = Path(td)
    global_conv = root / "global-converted"
    global_conv.mkdir()
    global_sentinel = global_conv / "tsn_highway_log_do_not_touch.xlsx"
    global_note = global_conv / "keep.txt"
    global_sentinel.write_bytes(b"GLOBAL-XLSX-SENTINEL")
    global_note.write_bytes(b"GLOBAL-NOTE-SENTINEL")
    original_global = snapshot(global_conv)

    records = {}
    record_lock = threading.Lock()
    parse_barrier = None
    write_barrier = None

    def fake_parse(path, events, pdf_name=""):
        del path, events
        source = Path(pdf_name).stem.split("_")[-1]
        if source == "BAD":
            raise ValueError("deliberate parse failure")
        if parse_barrier is not None and source in {"A", "B"}:
            parse_barrier.wait(timeout=5)
        district = {"A": "01", "B": "02"}.get(source, "03")
        route = {"A": "001", "B": "002"}.get(source, "003")
        return district, {route: [{"source": source}]}

    def fake_write(rows, out_path):
        out_path = Path(out_path)
        out_path.write_text(rows[0]["source"], encoding="utf-8")
        if write_barrier is not None and rows[0]["source"] in {"A", "B"}:
            write_barrier.wait(timeout=5)

    def fake_combine(**kwargs):
        manifest = kwargs.get("input_files")
        explicit = manifest is not None
        members = (list(manifest) if explicit
                   else list(Path(kwargs["input_dir"]).glob("*.xlsx")))
        members = sorted((Path(p) for p in members), key=lambda p: p.name)
        sources = [p.read_text(encoding="utf-8") for p in members]
        out = Path(kwargs["out_path"])
        with record_lock:
            records[out.name] = {
                "explicit": explicit,
                "input_dir": Path(kwargs["input_dir"]),
                "names": [p.name for p in members],
                "sources": sources,
            }
        if out.stem.endswith("combine_failure"):
            return ConsolidateResult(status="error", message="deliberate combine failure")
        out.parent.mkdir(parents=True, exist_ok=True)
        out.write_text(",".join(sources), encoding="utf-8")
        return ConsolidateResult(status="ok", output_path=str(out),
                                 summary_lines=[f"Members: {len(members)}"])

    with patch.object(target, "_DEPS_OK", True), \
            patch.object(target, "CONVERTED_DIR", global_conv), \
            patch.object(target, "parse_pdf", fake_parse), \
            patch.object(target, "_write_route_workbook", fake_write), \
            patch.object(target, "consolidate_xlsx", fake_combine), \
            patch.object(target.tdc, "require_exact_universe",
                         lambda claimed: tuple(claimed)):

        # A custom output is already a complete requested build boundary.  It
        # must not use the unrelated module-global conversion store at all.
        raw_single = root / "raw-single"
        touch_pdf(raw_single, "D03_S.pdf")
        out_single = root / "single-build" / "single.xlsx"
        explicit_boundary = root / "explicit-build-boundary"
        single = target.consolidate(input_dir=raw_single, out_path=out_single,
                                    scratch_root=explicit_boundary)
        check("custom-output build succeeds", single.status == "ok")
        check("custom-output build preserves the global converted directory",
              snapshot(global_conv) == original_global)
        check("custom-output combine receives an explicit member manifest",
              records.get("single.xlsx", {}).get("explicit") is True)
        check("custom-output manifest is exactly its generated route member",
              records.get("single.xlsx", {}).get("sources") == ["S"])
        check("attempt scratch is allocated under the explicit build boundary",
              records.get("single.xlsx", {}).get("input_dir", Path()).parent
              == explicit_boundary.absolute())
        check("custom-output attempt scratch is removed",
              no_subdirs(explicit_boundary))

        # Restore only this disposable fixture before the overlap probe so the
        # old implementation's first failure cannot make the concurrency result
        # timing-dependent.
        shutil.rmtree(global_conv)
        global_conv.mkdir()
        global_sentinel.write_bytes(b"GLOBAL-XLSX-SENTINEL")
        global_note.write_bytes(b"GLOBAL-NOTE-SENTINEL")
        original_global = snapshot(global_conv)

        raw_a, raw_b = root / "raw-a", root / "raw-b"
        touch_pdf(raw_a, "D01_A.pdf")
        touch_pdf(raw_b, "D02_B.pdf")
        overlap_boundary = root / "overlap-build"
        out_a = overlap_boundary / "a.xlsx"
        out_b = overlap_boundary / "b.xlsx"
        parse_barrier = threading.Barrier(2)
        write_barrier = threading.Barrier(2)
        results = {}
        errors = []

        def run(name, raw, out):
            try:
                results[name] = target.consolidate(input_dir=raw, out_path=out)
            except Exception as exc:  # failure is reported below, not lost in a thread
                errors.append((name, type(exc).__name__, str(exc)))

        ta = threading.Thread(target=run, args=("a", raw_a, out_a), daemon=True)
        tb = threading.Thread(target=run, args=("b", raw_b, out_b), daemon=True)
        ta.start()
        tb.start()
        ta.join(10)
        tb.join(10)
        parse_barrier = write_barrier = None

        check("overlapping attempts terminate without exception or deadlock",
              not ta.is_alive() and not tb.is_alive() and not errors)
        check("overlapping attempts both succeed",
              set(results) == {"a", "b"}
              and all(r.status == "ok" for r in results.values()))
        check("attempt A consumes only its exact member",
              records.get("a.xlsx", {}).get("explicit") is True
              and records.get("a.xlsx", {}).get("sources") == ["A"])
        check("attempt B consumes only its exact member",
              records.get("b.xlsx", {}).get("explicit") is True
              and records.get("b.xlsx", {}).get("sources") == ["B"])
        check("overlapping custom builds preserve global sentinels",
              snapshot(global_conv) == original_global)
        check("overlapping attempt scratch directories are removed",
              all(no_subdirs(out.parent) for out in (out_a, out_b)))

        # Cancellation/failure must clean the private attempt.  A retry in the
        # same build boundary must see no member from the failed attempt.
        raw_cancel = root / "raw-cancel"
        touch_pdf(raw_cancel, "D03_CANCEL.pdf")
        cancel_out = root / "cancel-build" / "cancel.xlsx"
        cancel = target.consolidate(
            input_dir=raw_cancel, out_path=cancel_out,
            events=Events(is_cancelled=lambda: True))
        check("cancellation returns cancelled and leaves no scratch",
              cancel.status == "cancelled"
              and no_subdirs(cancel_out.parent))

        raw_retry = root / "raw-retry"
        touch_pdf(raw_retry, "D03_BAD.pdf")
        retry_out = root / "retry-build" / "retry.xlsx"
        failed = target.consolidate(input_dir=raw_retry, out_path=retry_out)
        check("parse failure leaves no scratch",
              failed.status == "error"
              and no_subdirs(retry_out.parent))
        (raw_retry / "D03_BAD.pdf").unlink()
        touch_pdf(raw_retry, "D03_RETRY.pdf")
        retried = target.consolidate(input_dir=raw_retry, out_path=retry_out)
        check("retry consumes only the retry generation",
              retried.status == "ok"
              and records.get("retry.xlsx", {}).get("sources") == ["RETRY"]
              and no_subdirs(retry_out.parent))

        raw_comb = root / "raw-combine-failure"
        touch_pdf(raw_comb, "D03_COMBFAIL.pdf")
        comb_out = root / "combine-failure-build" / "combine_failure.xlsx"
        comb = target.consolidate(input_dir=raw_comb, out_path=comb_out)
        check("combine failure returns error and leaves no scratch",
              comb.status == "error"
              and no_subdirs(comb_out.parent))

        decline_out = root / "decline-build" / "decline.xlsx"
        decline_out.parent.mkdir(parents=True)
        decline_out.write_bytes(b"LAST-GOOD")
        declined = target.consolidate(
            input_dir=raw_single, out_path=decline_out,
            confirm_overwrite=lambda _path: False)
        check("overwrite decline keeps last-good and allocates no scratch",
              declined.status == "cancelled"
              and decline_out.read_bytes() == b"LAST-GOOD"
              and no_subdirs(decline_out.parent))

        guard_out = root / "guard-build" / "guard.xlsx"

        def lose_guard_for_attempt(path):
            return ".tsn-highway-log-attempt-" not in str(path)

        guarded = target.consolidate(
            input_dir=raw_single, out_path=guard_out,
            commit_guard=lose_guard_for_attempt)
        check("attempt guard loss fails closed and cleans identity-bound scratch",
              guarded.status == "error" and not guard_out.exists()
              and no_subdirs(guard_out.parent))
        check("all terminal paths preserve the global converted directory",
              snapshot(global_conv) == original_global)

    # Exercise the real manifest consumer too: an unlisted but perfectly valid
    # sibling workbook must not enter the consolidated output.
    member_dir = root / "real-members"
    member_dir.mkdir()

    def workbook(path, value):
        wb = Workbook()
        ws = wb.active
        ws.title = "Highway Log"
        ws.append(["Location"])
        ws.append([value])
        wb.save(path)
        wb.close()

    listed = member_dir / "tsn_highway_log_d01_route_001.xlsx"
    unlisted = member_dir / "tsn_highway_log_d02_route_002.xlsx"
    workbook(listed, "LISTED")
    workbook(unlisted, "UNLISTED")
    real_out = root / "real-manifest.xlsx"
    real_result = real_consolidate_xlsx(
        input_dir=member_dir, input_files=(listed,), out_path=real_out,
        sheet_name="Highway Log", report_name="TSN Highway Log",
        title="Manifest fixture")
    wb = load_workbook(real_out, read_only=True, data_only=True)
    try:
        rows = list(wb["Highway Log"].iter_rows(min_row=2, values_only=True))
    finally:
        wb.close()
    check("real consolidation consumes the explicit manifest, not valid siblings",
          real_result.status == "ok" and rows == [("001", "LISTED")])

    outside = root / "outside_route_999.xlsx"
    workbook(outside, "OUTSIDE")
    invalid = real_consolidate_xlsx(
        input_dir=member_dir, input_files=(outside,),
        out_path=root / "invalid-manifest.xlsx", sheet_name="Highway Log",
        report_name="TSN Highway Log", title="Invalid manifest fixture")
    check("manifest rejects members outside the attempt directory",
          invalid.status == "error" and not (root / "invalid-manifest.xlsx").exists())


if failures:
    raise SystemExit(f"{len(failures)} TSN Highway Log isolation check(s) failed")
print("TSN Highway Log attempt isolation (CMP-AUD-132): PASS")
